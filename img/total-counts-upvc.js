
function findTotalupvcorders()
{
	var arr = document.getElementsByName('utotal');
	var tot = 0;
	for(var i = 0; i<arr.length; i++)
	{
		if(parseInt(arr[i].value))
			tot += parseInt(arr[i].value);
	}
	document.getElementById('total-upvc-order').value = tot;
}

