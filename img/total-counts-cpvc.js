function findTotalPipe1()
{
	var arr = document.getElementsByName('pipe1');
	var tot = 0;
	for(var i = 0; i<arr.length; i++)
	{
		if(parseInt(arr[i].value))
			tot += parseInt(arr[i].value);
	}
	document.getElementById('total-pipe1').value = tot;
}

